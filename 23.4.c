#include <stdio.h>
int main(void) 
{
    char a = 64 & 127;  //按位与，结果为01000000，即64
    char b = 64 ^ 127;  //按位异或，结果为00111111，即63
    char c = -64 >> 6;  //负数在计算机中以补码表示，假设 char 为 8 位：
//                        -64 的原码为 11000000，补码为 11000000（原码符号位不变，其余位取反加 1）。
//                        右移 6 位（算术右移，符号位填充）：
//                        补码 11000000 右移 6 位后为 11111111（即 -1 的补码），因此 c = -1。
    char ch = a + b - c;  //8位char的范围为-128~127，128溢出后实际存储为-128
    printf("a = %d b = %d c = %d\n", a, b, c);
    printf("ch = %d\n", ch);
}
//运行结果如下
//a = 64 b = 63 c = -1
//ch = -128